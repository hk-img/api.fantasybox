<script>
    function getfantasytype(selectElement) {
        var selectedOption = selectElement.options[selectElement.selectedIndex];
        var selectedName = selectedOption.textContent.trim();
        // Encode the selectedName using Base64
        var encodedName = btoa(selectedName);
        console.log(encodedName);
        window.location.href = '{{ route('series.view', ':encodedName') }}'.replace(':encodedName', encodedName);
    }
</script>

<script>
    function multiselect(mid,fantasy_type) {
        var p = [];
        $.each($("input[name='checkcontest']:checked"), function() {
            p.push($(this).val());
        });
        console.log(p);
        if (p != "") {
            swal({
                title: "Are you sure?",
                text: "You want to Import Contest!",
                icon: "error",
                buttons: {
                    confirm: {
                        className: "btn-danger",
                        text: "Yes",
                        value: true,
                    },
                    cancel: true,
                },
            }).then((confirmed) => {
                if (confirmed) {
                    $.ajax({
                        type: "POST",
                        headers: {
                            'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                        },
                        url: "{{ route('match.importmatchcontest') }}",
                        data: {
                            _token: "{{ csrf_token() }}",
                            matchkey: mid,
                            contests: p
                        },
                        success: function(data) {
                            swal("Contest has been Imported!", {
                                icon: "success",
                            }).then(() => {
                                window.location.href = "../customcontest/" + fantasy_type + "?matchkey="+mid;
                            });
                        },
                        error: function(xhr, textStatus, errorThrown) {
                            swal("Error Occured!");
                        }
                    });
                } else {
                    swal("Decline Import Request!");
                }
            });
        } else {
            swal('Please select atlease one contest to import');
        }
    }
</script>
